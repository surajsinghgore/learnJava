public class Program27 {
    public static void main(String[] args) {
        int arr[];
        arr=new int[5];
        arr[0]=10;
        arr[1]=20;
        arr[2]=30;
        arr[3]=40;
        arr[4]=50;
        System.out.println("One Dimensional Array");
        for (int i = 0; i < 5; i++) {
            System.out.println("arr["+i+" ] = "+arr[i]);
        }
    }
}
